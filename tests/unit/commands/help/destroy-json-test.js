'use strict';

var expect         = require('chai').expect;
var MockUI         = require('../../../helpers/mock-ui');
var MockAnalytics  = require('../../../helpers/mock-analytics');
var convertToJson  = require('../../../helpers/convert-help-output-to-json');
var HelpCommand    = require('../../../../lib/commands/help');
var DestroyCommand = require('../../../../lib/commands/destroy');

describe('help command: destroy json', function() {
  var ui, command;

  beforeEach(function() {
    ui = new MockUI();

    var options = {
      ui: ui,
      analytics: new MockAnalytics(),
      commands: {
        'Destroy': DestroyCommand
      },
      project: {
        isEmberCLIProject: function() {
          return true;
        }
      },
      settings: {}
    };

    command = new HelpCommand(options);
  });

  it('works', function() {
    return command.validateAndRun(['destroy', '--json']).then(function() {
      var json = convertToJson(ui.output);

      var command = json.commands[0];
      expect(command).to.deep.equal({
        name: 'destroy',
        description: 'Destroys code generated by `generate` command.',
        aliases: ['d'],
        works: 'insideProject',
        availableOptions: [
          {
            name: 'dry-run',
            default: false,
            aliases: ['d'],
            key: 'dryRun',
            required: false
          },
          {
            name: 'verbose',
            default: false,
            aliases: ['v'],
            key: 'verbose',
            required: false
          },
          {
            name: 'pod',
            default: false,
            aliases: ['p'],
            key: 'pod',
            required: false
          },
          {
            name: 'classic',
            default: false,
            aliases: ['c'],
            key: 'classic',
            required: false
          },
          {
            name: 'dummy',
            default: false,
            aliases: ['dum', 'id'],
            key: 'dummy',
            required: false
          },
          {
            name: 'in-repo-addon',
            default: null,
            aliases: ['in-repo', 'ir'],
            key: 'inRepoAddon',
            required: false
          }
        ],
        anonymousOptions: ['<blueprint>']
      });
    });
  });

  it('works with alias d', function() {
    return command.validateAndRun(['d', '--json']).then(function() {
      var json = convertToJson(ui.output);

      var command = json.commands[0];
      expect(command.name).to.equal('destroy');
    });
  });
});
