'use strict';

var path          = require('path');
var tmp           = require('tmp-sync');
var expect        = require('chai').expect;
var ember         = require('../../helpers/ember');
var convertToJson = require('../../helpers/convert-help-output-to-json');
var Promise       = require('../../../lib/ext/promise');
var remove        = Promise.denodeify(require('fs-extra').remove);
var root          = process.cwd();
var tmproot       = path.join(root, 'tmp');
var tmpdir;

describe('Acceptance: ember help --json destroy', function() {
  beforeEach(function() {
    tmpdir = tmp.in(tmproot);
    process.chdir(tmpdir);
  });

  afterEach(function() {
    process.chdir(root);
    return remove(tmproot);
  });

  it('works', function() {
    return ember([
      'help',
      'destroy',
      '--json'
    ])
    .then(function(result) {
      var json = convertToJson(result.ui.output);

      var command = json.commands[0];
      expect(command).to.deep.equal({
        name: 'destroy',
        description: 'Destroys code generated by `generate` command.',
        aliases: ['d'],
        works: 'insideProject',
        availableOptions: [
          {
            name: 'dry-run',
            default: false,
            aliases: ['d'],
            key: 'dryRun',
            required: false
          },
          {
            name: 'verbose',
            default: false,
            aliases: ['v'],
            key: 'verbose',
            required: false
          },
          {
            name: 'pod',
            default: false,
            aliases: ['p'],
            key: 'pod',
            required: false
          },
          {
            name: 'classic',
            default: false,
            aliases: ['c'],
            key: 'classic',
            required: false
          },
          {
            name: 'dummy',
            default: false,
            aliases: ['dum', 'id'],
            key: 'dummy',
            required: false
          },
          {
            name: 'in-repo-addon',
            default: null,
            aliases: ['in-repo', 'ir'],
            key: 'inRepoAddon',
            required: false
          }
        ],
        anonymousOptions: ['<blueprint>']
      });
    });
  });

  it('works with alias d', function() {
    return ember([
      'help',
      'd',
      '--json'
    ])
    .then(function(result) {
      var json = convertToJson(result.ui.output);

      var command = json.commands[0];
      expect(command.name).to.equal('destroy');
    });
  });
});
